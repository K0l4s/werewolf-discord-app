export function wolfCoin(number) {
    let amount = Number(number)
    return `${amount.toLocaleString("en-US")} <:wolf_coin:1400508720160702617>`
}
export function wolfToken(number) {
    let amount = Number(number)
    return `${amount.toLocaleString("en-US")} <:token:1430890033409163294>`
}
export function wolfIcon() {
    return `<:wolf_coin:1400508720160702617>`
}
export function wolfTokenIcon() {
    return `<:token:1430890033409163294>`
}